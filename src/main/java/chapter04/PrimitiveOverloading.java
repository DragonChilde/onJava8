/** @title: PrimitiveOverloading @Author Wen @Date: 2020/11/15 23:43 @Version 1.0 */
public class PrimitiveOverloading {
  void f1(char x) {
    System.out.println("f1(char)");
  }

  void f1(byte x) {
    System.out.println("f1(byte)");
  }

  void f1(short x) {
    System.out.println("f1(short)");
  }
}
